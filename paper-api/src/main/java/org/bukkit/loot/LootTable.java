package org.bukkit.loot;

import io.papermc.paper.loot.LootContextKey;
import java.util.Collection;
import java.util.Random;
import java.util.Set;
import org.bukkit.Keyed;
import org.bukkit.inventory.Inventory;
import org.bukkit.inventory.ItemStack;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import org.jetbrains.annotations.Unmodifiable;

/**
 * LootTables are technical files that represent what items should be in
 * naturally generated containers, what items should be dropped when killing a
 * mob, or what items can be fished.
 * <br>
 * See the <a href="https://minecraft.wiki/w/Loot_table">
 * Minecraft Wiki</a> for more information.
 */
public interface LootTable extends Keyed {

    /**
     * Returns a mutable list of loot generated by this LootTable.
     *
     * @param random the random instance to use to generate loot
     * @param context context within to populate loot
     * @return a list of ItemStacks
     * @deprecated the random instance is provided through the loot context, use {@link #populateLoot(LootContext)}
     */
    @Deprecated(since = "26.1")
    @NotNull Collection<ItemStack> populateLoot(final @Nullable Random random, final @NotNull LootContext context);

    /**
     * Returns a mutable list of loot generated by this LootTable.
     *
     * @param context context within to populate loot
     * @return a list of ItemStacks
     */
    @NotNull Collection<ItemStack> populateLoot(@NotNull LootContext context);

    /**
     * Attempt to fill an inventory with this LootTable's loot.
     *
     * @param inventory the inventory to fill
     * @param random the random instance to use to generate loot
     * @param context context within to populate loot
     * @deprecated the random instance is provided through the loot context, use {@link #fillInventory(Inventory, LootContext)}
     */
    @Deprecated(since = "26.1")
    void fillInventory(@NotNull Inventory inventory, @Nullable Random random, @NotNull LootContext context);

    /**
     * Attempt to fill an inventory with this LootTable's loot.
     *
     * @param inventory the inventory to fill
     * @param context context within to populate loot
     */
    void fillInventory(@NotNull Inventory inventory, @NotNull LootContext context);

    /**
     * Gets the loot context keys required to be set in {@link LootContext} for this loot table.
     *
     * @return the required keys
     */
    @NotNull @Unmodifiable Set<LootContextKey> requiredContextKeys();

    /**
     * Gets the required and optional loot context keys to be set in {@link LootContext} for this loot table.
     *
     * @return the allowed keys
     */
    @NotNull @Unmodifiable Set<LootContextKey> allowedContextKeys();
}
